<?php 

namespace App\Models;

use Illuminate\Support\MessageBag;

trait Managed {

	static function bootManaged()
	{

	}

	// --------------------------------------------------------------------
	// RELATIONS
	// --------------------------------------------------------------------
	public function managed()
	{
		return $this->belongsToMany(__NAMESPACE__ . '\User')->withTimestamps()->orderBy('pivot_created_at', 'asc');
	}

	// --------------------------------------------------------------------
	// SCOPE
	// --------------------------------------------------------------------
	public function scopeManagedById($q, $v)
	{
		if (!$v)
		{
			return $q;
		}
		else
		{
			return $q->where('managed', function($q) use ($v) {
				$q->whereIn(User::getTable() . '.id', is_array($v) ? $v : [$v] );
			});
		}
	}


	// --------------------------------------------------------------------
	// ACCESSOR
	// --------------------------------------------------------------------

	// --------------------------------------------------------------------
	// MUTATOR
	// --------------------------------------------------------------------

	// --------------------------------------------------------------------
	// FUNCTIONS
	// --------------------------------------------------------------------
}

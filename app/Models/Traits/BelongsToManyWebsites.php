<?php 

namespace App\Models;

use Illuminate\Support\MessageBag;

trait BelongsToManyWebsites {

	static function bootBelongsToManyWebsites()
	{

	}

	// --------------------------------------------------------------------
	// RELATIONS
	// --------------------------------------------------------------------
	public function websites()
	{
		return $this->belongsToMany(__NAMESPACE__ . '\Website');
	}

	// --------------------------------------------------------------------
	// SCOPE
	// --------------------------------------------------------------------
	public function scopeBelongsToWebsiteId($q, $v)
	{
		if (is_null($v))
		{
			return $q;
		}
		else
		{
			return $q->whereIn('websites', function($q) use ($v) {
				$q->whereIn(Website::getTable() . '.id', is_array($v) ? $v : [$v]);
			});
		}
	}

	// --------------------------------------------------------------------
	// ACCESSOR
	// --------------------------------------------------------------------

	// --------------------------------------------------------------------
	// MUTATOR
	// --------------------------------------------------------------------

	// --------------------------------------------------------------------
	// FUNCTIONS
	// --------------------------------------------------------------------
}

<?php 

namespace App\Models;

use Illuminate\Support\MessageBag;

trait BelongsToManyContents {

	static function bootBelongsToManyContents()
	{

	}

	// --------------------------------------------------------------------
	// RELATIONS
	// --------------------------------------------------------------------
	public function contents()
	{
		return $this->belongsToMany(__NAMESPACE__ . '\Content');
	}

	// --------------------------------------------------------------------
	// SCOPE
	// --------------------------------------------------------------------
	public function scopeBelongsToContentsId($q, $v)
	{
		if (!$v)
		{
			return $q;
		}
		else
		{
			return $q->whereIn('contents', function($q) use ($v) {
				$q->whereIn(Content::getTable() . '.id', is_array($v) ? $v : [$v]);
			});
		}
	}

	// --------------------------------------------------------------------
	// ACCESSOR
	// --------------------------------------------------------------------

	// --------------------------------------------------------------------
	// MUTATOR
	// --------------------------------------------------------------------

	// --------------------------------------------------------------------
	// FUNCTIONS
	// --------------------------------------------------------------------
}
